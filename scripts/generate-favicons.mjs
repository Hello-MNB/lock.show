// FAVICON REGENERATION (W-2 fix 4) — rasterizes the governed brand master
// (public/assets/brand/lockshow-symbol-spotlight-lens-v2-lime-on-ink.svg,
// ink bg + lime spotlight-lens mark) into every app icon size, and mirrors
// the output byte-identically into website-next/public so both projects
// serve the SAME bytes (no drift between app.lock.show and lock.show).
//
// Requires `sharp` (SVG→PNG raster) and `to-ico` (PNG[]→.ico) at run time —
// install once with:  npm i --no-save sharp to-ico
// Ready-to-run: `node scripts/generate-favicons.mjs`
//
// MASKABLE SAFE ZONE: Android/PWA launchers crop a `purpose:maskable` icon to
// an arbitrary shape (circle, squircle, rounded-square…) and only GUARANTEE
// the inner circle at 80% of the canvas diameter (the "safe zone" — see
// https://web.dev/maskable-icon/). icon-maskable-512.png pads the mark to
// ~66% of the canvas (comfortably inside that 80% circle) on a solid ink
// canvas matching the mark's own background, so the padding reads as part of
// the icon rather than a visible border.
import { readFile, writeFile, mkdir } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = fileURLToPath(new URL('..', import.meta.url))
const SVG_SRC = join(ROOT, 'public/assets/brand/lockshow-symbol-spotlight-lens-v2-lime-on-ink.svg')
const INK = '#0A0D0B' // matches the SVG's own <rect> background — see the SVG source

// Byte-identical mirrors: every generated file is written once, then copied
// (not re-rendered) to the website-next mirror — "identical" isn't left to
// two separate raster passes agreeing by chance.
const TARGETS = [
  { name: 'favicon-32.png', size: 32 },
  { name: 'icon-192.png', size: 192 },
  { name: 'icon-512.png', size: 512 },
  { name: 'apple-touch-icon.png', size: 180 },
]

async function main() {
  let sharp, toIco
  try {
    ;({ default: sharp } = await import('sharp'))
  } catch {
    console.log('✗ FAVICONS SKIPPED — `sharp` is not installed. Run: npm i --no-save sharp to-ico')
    console.log('  This script is ready-to-run once that dependency is available; no output was written.')
    process.exit(0)
  }
  try {
    ;({ default: toIco } = await import('to-ico'))
  } catch {
    toIco = null // .ico is best-effort; PNGs still get generated without it
  }

  const svg = await readFile(SVG_SRC)
  // Three mirrors: the standalone app's own public/, the marketing site's
  // public/ (site-root favicon), and website-next/public/app/ — the PRE-BUILT
  // embed bundle committed for lock.show/app (see git history: "self-contained
  // website build — commit pre-built /app bundle"). That embed copy is a build
  // artifact, not regenerated by any build step run here, so it goes stale
  // silently unless this script refreshes it too — included only if present.
  const outDirs = [join(ROOT, 'public'), join(ROOT, 'website-next/public')]
  const embedDir = join(ROOT, 'website-next/public/app')
  if (existsSync(embedDir)) outDirs.push(embedDir)
  for (const d of outDirs) if (!existsSync(d)) await mkdir(d, { recursive: true })

  const written = []

  // Plain square icons (favicon/192/512/apple-touch) — the mark already fills
  // its own rounded-square canvas edge-to-edge, so these are a direct resize.
  for (const { name, size } of TARGETS) {
    const buf = await sharp(svg, { density: 384 }).resize(size, size).png().toBuffer()
    for (const dir of outDirs) await writeFile(join(dir, name), buf)
    written.push(name)
  }

  // Maskable 512 — pad the mark to ~66% of the canvas on a solid ink field so
  // it survives any OS mask shape without clipping the lens motif.
  const MASK_SIZE = 512
  const MARK_SIZE = Math.round(MASK_SIZE * 0.66)
  const markBuf = await sharp(svg, { density: 384 }).resize(MARK_SIZE, MARK_SIZE).png().toBuffer()
  const maskableBuf = await sharp({
    create: { width: MASK_SIZE, height: MASK_SIZE, channels: 4, background: INK },
  })
    .composite([{ input: markBuf, gravity: 'center' }])
    .png()
    .toBuffer()
  for (const dir of outDirs) await writeFile(join(dir, 'icon-maskable-512.png'), maskableBuf)
  written.push('icon-maskable-512.png')

  // website-next/app/favicon.ico — Next.js app-router favicon convention.
  // Multi-size .ico (16/32/48/64) built from the same raster pipeline.
  if (toIco) {
    const icoSizes = [16, 32, 48, 64]
    const icoPngs = await Promise.all(
      icoSizes.map((s) => sharp(svg, { density: 384 }).resize(s, s).png().toBuffer())
    )
    const icoBuf = await toIco(icoPngs)
    await writeFile(join(ROOT, 'website-next/app/favicon.ico'), icoBuf)
    written.push('website-next/app/favicon.ico')
  } else {
    console.log('  (skipped favicon.ico — `to-ico` not installed; PNGs above are still fresh)')
  }

  console.log(`✓ FAVICONS: regenerated from ${SVG_SRC.replace(ROOT, '')} → ${written.join(', ')}`)
  console.log(`  Mirrored byte-identically into: ${outDirs.map((d) => d.replace(ROOT, '')).join(', ')}`)
}

main().catch((e) => {
  console.error('✗ FAVICONS failed:', e?.message || e)
  process.exit(1)
})
